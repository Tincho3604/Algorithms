-- PUNTO 13: Crear una vista que permita listar los nombres y apellidos 
-- de cada uno de los empleados ordenados por departamentos y apellido, 
-- indicando el sueldo promedio que le corresponde a su tarea y el salario que recibe.

CREATE OR REPLACE VIEW V_PUNTO_13 AS 
SELECT 
 EMP.NOMBRE, 
 EMP.APELLIDO, 
 TASK.TITULO,
 AVG((TASK.SALARIO_MIN + TASK.SALARIO_MAX) / 2) AS SUELDO_PROMEDIO
FROM EMPLEADOS EMP 
LEFT JOIN DEPARTAMENTOS DEPA
ON EMP.DPTO_ID = DEPA.DPTO_ID
LEFT JOIN TAREAS TASK
ON EMP.TAREA_ID = TASK.TAREA_ID
GROUP BY 
 EMP.NOMBRE, EMP.APELLIDO, DEPA.NOMBRE, TASK.TITULO
ORDER BY 
 DEPA.NOMBRE, EMP.APELLIDO ASC;

-- PUNTO 14: Crear una vista que permita listar los nombres de cada uno de los departamentos con la cantidad de empleados que en ellos trabajan y el sueldo promedio de los empleados.
CREATE OR REPLACE VIEW V_PUNTO_14 AS 
SELECT DEPA.NOMBRE, 
COUNT(EMP.DPTO_ID) as CANTIDAD_EMPLEADOS,
ROUND(AVG((TASK.SALARIO_MIN + TASK.SALARIO_MAX) / 2)) AS SUELDO_PROMEDIO
FROM EMPLEADOS EMP 
INNER JOIN DEPARTAMENTOS DEPA 
ON EMP.DPTO_ID = DEPA.DPTO_ID
INNER JOIN TAREAS TASK
ON EMP.TAREA_ID = TASK.TAREA_ID
GROUP BY DEPA.NOMBRE;


